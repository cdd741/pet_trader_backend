const mongoose = require("mongoose");

const userAccountSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
});

const UserAccount = mongoose.model("UserAccount", userAccountSchema);

module.exports = UserAccount;
